# Параметры эксперимента по прогнозированию уровня глюкозы (OhioT1DM, пациент 588).
# Все константы собраны здесь, чтобы изменение любого параметра
# не требовало правки в нескольких файлах.

from pathlib import Path

# ─── Пути ────────────────────────────────────────────────────────────────────

# Корень проекта — папка на уровень выше src/
ROOT_DIR    = Path(__file__).resolve().parent.parent

# Путь к исходному XML-файлу пациента 588
DATA_PATH   = ROOT_DIR / "data" / "588-ws-training.xml"

# Папка для сохранения результатов (CSV и PNG)
RESULTS_DIR = ROOT_DIR / "results"


# ─── Основные параметры эксперимента ─────────────────────────────────────────

# Горизонты прогнозирования в шагах (1 шаг = 5 минут)
# h=1 → 5 мин, h=6 → 30 мин, h=12 → 60 мин
HORIZONS = list(range(1, 13))

# Максимальный горизонт — нужен ARIMA и Хольту для одного многошагового
# прогноза, из которого извлекаются значения для всех h.
MAX_H = max(HORIZONS)

# Длина окна истории для XGBoost и LSTM (12 шагов = 60 минут)
WINDOW_K = 12

# Доля ряда для обучения; остаток идёт на тест
TRAIN_RATIO = 0.80

# Порог скачка ΔMAE для определения горизонта деградации.
# Скачок считается значимым, если ΔMAE(h) > KAPPA * среднее предыдущих ΔMAE.
KAPPA = 2.0

# Фиксированное зерно для воспроизводимости результатов (XGBoost, LSTM)
SEED = 12


# ─── Параметры ARIMA ──────────────────────────────────────────────────────────

# Максимальный порядок p и q при переборе по критерию AIC.
# Высокие порядки на CGM-ряде одного пациента увеличивают риск переобучения.
ARIMA_MAX_ORDER = 3


# ─── Параметры XGBoost ───────────────────────────────────────────────────────

# Количество деревьев в ансамбле
XGB_TREES = 200

# Максимальная глубина дерева
XGB_DEPTH = 4

# Шаг обучения
XGB_LR = 0.05


# ─── Параметры LSTM ──────────────────────────────────────────────────────────

# Размер скрытого состояния
LSTM_HIDDEN = 32

# Количество рекуррентных слоёв
LSTM_LAYERS = 1

# Число эпох обучения
LSTM_EPOCHS = 30

# Размер батча
LSTM_BATCH = 64

# Шаг обучения для оптимизатора Adam
LSTM_LR = 1e-3

# ─── Режим отладки ────────────────────────────────────────────────────────────
DEBUG_MODE = False

TEST_LIMIT = 20 if DEBUG_MODE else 2528
